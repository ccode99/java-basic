package variable;

public class Var2 {
    public static void main(String[] args) {
        int a = 100;
        double b = 10.5;
        boolean c = true;
        char d = 'A';
        String e = "Hello Java!";

        byte bb = 127; //-128 ~ 127
        short s = 32767; // -32768 ~ 32767
        int i = 2147483647; // -2,147,483,648 ~ 2,147,483,647
        long l = 9223372036854775807L;
        float f = 10.0f;
        double dd = 10.0;

        /*
        1byte: byte, boolean, char
        4byte: int, float
        8byte: long, double
        */
    }
}
