package operator;

public class Operator2 {
    public static void main(String[] args) {
        String a = "Hello";
        System.out.println("a = " + a);

        int b = 99;
        System.out.println(a + b);

        int c = 0;
        System.out.println("c = " + c);
        System.out.println(++c);
    }
}
