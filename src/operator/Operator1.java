package operator;

public class Operator1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        System.out.println(5/2.0);
    }
}
